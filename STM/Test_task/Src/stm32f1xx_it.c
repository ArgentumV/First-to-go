/**
  * @brief This function handles DMA1 channel1 global interrupt.
  */
void DMA1_Channel1_IRQHandler(void)
{
  /* USER CODE BEGIN DMA1_Channel1_IRQn 0 */

  /* USER CODE END DMA1_Channel1_IRQn 0 */
  HAL_DMA_IRQHandler(&hdma_adc1);
  /* USER CODE BEGIN DMA1_Channel1_IRQn 1 */
	
	// Get here after 100 consecutive ADC conversions
	// Computing average value 
	Local_Average = 0;
	for (uint8_t c=0; c<100; c++) Local_Average += Buffer[c];
	Local_Average = Local_Average/100;
	
	// Update PWM
	htim3.Instance -> CCR1 = Local_Average;
	
	// Adding value to 1 Second Average variable
	Global_Average+= Local_Average;

	if (++Counter >= 100){
		// Every 1 second
		Counter = 0;
		HAL_GPIO_TogglePin(LED_GPIO_Port,LED_Pin);
		
		Global_Average = Global_Average/100;
		Len = Convert( (uint16_t)Global_Average, Str);
		CDC_Transmit_FS(Str, Len);

		Global_Average = 0;
	}
	
	// Restarting DMA
	HAL_DMA_Start_IT (&hdma_adc1, (uint32_t)&hadc1.Instance->DR, (uint32_t)&Buffer, 100);
	
  /* USER CODE END DMA1_Channel1_IRQn 1 */
}

