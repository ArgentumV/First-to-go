/**
  * @brief Conversion from int to formatted string 
	* @param value: integer to convert 
  * @param buffer: target buffer
  * @retval Lenght of string 
  */

uint8_t Convert(uint16_t value, uint8_t* buffer)
{
	uint8_t i;
	uint16_t digit;
	uint16_t divisor;
	uint8_t printed = 0;
	uint8_t len=0;
	uint32_t temp;
	
	temp = (uint32_t)value * 3300 / 4096; // converting to mV
	
	value = (uint16_t) temp;
	
	if(value)
	{
		for(i = 0, divisor = 10000; i < 5u; i++)
		{
			digit = value/divisor;
			if(digit || printed)
			{
				*buffer++ = '0' + digit;
				value -= digit*divisor;
				printed = 1;
				len++;
			}
			divisor /= 10;
		}
	}
	else
	{
		*buffer++ = '0';
		len = 1;
	}
	*buffer++ = ' ';
	*buffer++ = 'm';
	*buffer++ = 'V';
	*buffer++ = '\r';
	*buffer = 0;
	return len+4;
}



